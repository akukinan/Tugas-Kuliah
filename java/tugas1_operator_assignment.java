public static void main(String[] args) {

    //Operator Assigment
    int x;
    x = 100;
    System.out.println("x = " + x);
    x += 5;
    System.out.println("x += " + x);
    x -= 5;
    System.out.println("x -= " + x);
    x /= 5;
    System.out.println("x /= " + x);
    x *= 5;
    System.out.println("x *= " + x);
    x %= 5;
    System.out.println("x %= " + x);
}
