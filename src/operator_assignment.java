public class operator_assignment {
    public static void main(String[] args) {
        //operator assigment
        int x;
        x= 100;
        System.out.println("x = " + x);
        x += 5;
        System.out.println("x += " + x);
        x -= 5;
        System.out.println("x -= " + x);
        x /= 5;
        System.out.println("x /= " + x);
        x *= 5;
        System.out.println("x *= " + x);
        x %= 5;
        System.out.println("x %= " + x);
    }
}
