public class operator_perbandingan {
    public static void main(String[] args) {

        //buat variable
        int x,y;
        boolean check;
        x=3; y=7;

        //operator perbandingan
        check= (x > y);
        System.out.println("status x < y : " +check);
        check= (x >=y);
        System.out.println("status x >= y : " +check);
        check= (x != y);
        System.out.println("status x != y : " +check);
        check= (x == y);
        System.out.println("status x == y : " +check);
    }
}
